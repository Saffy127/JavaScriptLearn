// You can declare a variable using any one of the three keywords: "var", "let" and "const"
var boolean = true;
let string = "Hello world!";
const pi = 3.14159;

// You can access a variable's value using its name
console.log(boolean);
console.log(string);
console.log(pi);

// You can assign a new value to a variable
boolean = false;
string = "The quick brown fox.";

console.log(boolean); // -> false
console.log(string);  // -> "The quick brown fox"

